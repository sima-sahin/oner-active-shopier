// 1) Tüm .jpg dosyalarını toplayalım
const images = import.meta.glob('../assets/collection/**/*.jpg', { eager: true });

// 2) Slug'a göre ilgili görselleri bulalım
function getImagesForSlug(slug) {
  const matched = [];
  
  for (const path in images) {
    // Dosya yolunda slug geçiyorsa (örnek: "foundations-jogger-black")
    // bu görseli matched listesine ekliyoruz.
    if (path.includes(slug)) {
      // Vite + import.meta.glob ile alınan resim nesnesi
      // bazen `images[path].default` şeklinde olabilir.
      matched.push(images[path].default || images[path]);
    }
  }
  // İsteğe bağlı: Dosya isimlerinde "-1", "-2" gibi sıralama varsa
  matched.sort((a, b) => a.localeCompare(b));

  return matched;
}

